    <div class="lg:w-3/5 p-sm">
        <p class="mb-sm">
            Trauma führt zu einer Dysregulation in unserem Autonomen
            Nervensystem und kann sich in Symptomen der Übererregung (ständiges
            „auf der Hut sein" und „sich wappnen", permanente innere Unruhe,
            erhöhte Grundanspannung, Angstzustände, Panik, erhöhte Reizbarkeit,
            Wutanfälle, starkes Kontrollbedürfnis, Angst vor Nähe, schnelle
            Reizüberflutung, Schreckhaftigkeit, Albträume, Schlafstörungen -
            „der Körper bleibt in Alarmbereitschaft") oder in Symptomen der
            Untererregung (chron. Müdigkeit, Erschöpfung, schnelle
            Überforderung, Neigung zu depressiven Verstimmungen, Scham- und
            Schuldgefühle, Schwierigkeit Grenzen zu setzen, Dissoziation,
            Erinnerungslücken, Gefühle von Abspaltung, Taubheit, Leere,
            Hoffnungslosigkeit) zeigen. Psychosomatisch können sich z.B. auch
            Reizdarm, erhöhter Blutdruck, Autoimmunerkrankungen, Fibromyalgie
            oder Migräne entwickeln. Traumafolgen zeigen sich auch in einem
            unsicheren Körpergefühl, durch das die Welt als unsicherer Ort
            interpretiert wird.
        </p>

        <p class="mb-sm">
            Aber Trauma geschieht nicht nur durch das, was uns passiert, sondern
            auch durch das, was uns nicht passiert, was wir aber hätten bekommen
            sollen. So kann fehlender Körperkontakt in den ersten Lebensjahren,
            fehlendes versorgt werden, fehlende emotionale Einstimmungsfähigkeit
            unserer ersten Bezugspersonen, fehlende Verlässlichkeit oder
            fehlendes Respektieren unserer Grenzen (physische/emotionale
            Vernachlässigung, das Fehlen eines grundlegenden Gefühles von
            Sicherheit) dazu führen, dass wir aufgrund fehlender Ko-Regulation
            nicht so gut lernen konnten, wie wir uns gut selbst regulieren
            können oder auch uns im Zusammensein mit anderen Menschen
            gegenseitig regulieren können.
        </p>

        <p class="italic text-gray-700 font-semibold">
            „Wir alle werden mit einer erstaunlichen Fähigkeit, Trauma
            überwinden zu können, geboren." – Peter Levine
        </p>
