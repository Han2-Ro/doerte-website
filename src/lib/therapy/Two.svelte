            <p class=" text-md leading-relaxed">
                „Wenn ein junger Baum verletzt wird, wächst er um die Verletzung
                herum weiter. Im weiteren Verlauf seiner Entwicklung wird die
                Wunde im Verhältnis zu seiner Größe immer kleiner. Knorrige
                Auswüchse und unförmige Äste deuten auf Verletzungen und
                Hindernisse hin, denen ein Baum im Laufe der Zeit ausgesetzt war
                und die er überwunden hat. Die Art, wie ein Baum um seine
                Vergangenheit herum wächst, trägt zu seinem Charakter und zu
                seiner Schönheit bei.“ - Peter Levine
